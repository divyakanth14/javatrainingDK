
public class Weekday {
	public static void main(String[] args) {
		byte i=0;
		if(i==1) {
			System.out.println("It is Monday: "+i);
		}else if (i==2) {
			System.out.println("It is Tuesday: "+i);
		}else if (i==3) {
			System.out.println("It is wednesday: "+i);
		}else if (i==4) {
			System.out.println("It is Thursday: "+i);
		}else if (i==5) {
			System.out.println("It is Friday: "+i);
		}else if (i==6) {
			System.out.println("It is Saturday: "+i);
		}else if (i==7) {
			System.out.println("It is Sunday: "+i);
		}else {
			System.out.println("Not an Week day: "+i);
		}
	}

}
